public class Greatest_Another {
    public static void main(String[] args) {

        int a = 80;
        int b = 50;
        int c = 130;

        if (a > b && a > c) {
            System.out.println("a is greater");
        } else if (b > c) {
            System.out.println("b is greater");
        } else {
            System.out.println("c is greater");
        }
    }
}